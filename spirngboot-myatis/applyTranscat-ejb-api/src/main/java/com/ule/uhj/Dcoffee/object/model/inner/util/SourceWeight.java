package com.ule.uhj.Dcoffee.object.model.inner.util;

import com.ule.uhj.Dcoffee.object.model.inner.source.Source;

/**
 * Created by zhengxin on 2018/3/14.
 */
public class SourceWeight{
    private Source source;
    private int weight;

    public SourceWeight(Source source, int weight) {
        this.source = source;
        this.weight = weight;
    }

    public Source getSource() {
        return source;
    }

    public int getWeight() {
        return weight;
    }
}