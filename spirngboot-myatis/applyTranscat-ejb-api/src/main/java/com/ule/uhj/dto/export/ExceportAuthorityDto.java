package com.ule.uhj.dto.export;

import java.io.Serializable;

public class ExceportAuthorityDto implements Serializable {

	private static final long serialVersionUID = 4059888372600868075L;
	private long Id;
	private String userOnlyId;
	private String exportId;

	public long getId() {
		return Id;
	}

	public void setId(long id) {
		Id = id;
	}

	public String getUserOnlyId() {
		return userOnlyId;
	}

	public void setUserOnlyId(String userOnlyId) {
		this.userOnlyId = userOnlyId;
	}

	public String getExportId() {
		return exportId;
	}

	public void setExportId(String exportId) {
		this.exportId = exportId;
	}

}
