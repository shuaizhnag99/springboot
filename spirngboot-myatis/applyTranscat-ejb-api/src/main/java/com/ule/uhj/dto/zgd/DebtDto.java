package com.ule.uhj.dto.zgd;

import java.io.Serializable;

public class DebtDto implements Serializable {
	
	private static final long serialVersionUID = 8165006240258150360L;

	/**
	 *记录id
	*/
	private String recordId;

	/**
	 *用户id
	*/
	private String userOnlyId;

	/**
	 *用户名	
	*/
	private String userName;

	/**
	 *金额	
	*/
	private String amount;

	/**
	 *债务机构	
	*/
	private String debtOrgan;

	/**
	 *利率	
	*/
	private String rate;

	/**
	 *期限
	*/
	private String period;
	
	/**
	 *记录创建时间	
	*/
	private String createTime;

	/**
	 *记录更新时间	
	*/
	private String updateTime;

	/**
	 *备注	
	*/
	private String remark;

	public String getRecordId() {
		return recordId;
	}

	public void setRecordId(String recordId) {
		this.recordId = recordId;
	}

	public String getUserOnlyId() {
		return userOnlyId;
	}

	public void setUserOnlyId(String userOnlyId) {
		this.userOnlyId = userOnlyId;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getDebtOrgan() {
		return debtOrgan;
	}

	public void setDebtOrgan(String debtOrgan) {
		this.debtOrgan = debtOrgan;
	}

	public String getRate() {
		return rate;
	}

	public void setRate(String rate) {
		this.rate = rate;
	}

	public String getPeriod() {
		return period;
	}

	public void setPeriod(String period) {
		this.period = period;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

}
